#include "libft.h"

int	ft_atoi(const char *str)
{
	int n;
	int flag;

	n = 0;
	flag = 1;
	while (*str == '\n' || *str == '\f' || *str == '\r'
			|| *str == '\t' || *str == '\v' || *str == ' ')
		str++;
	if (*str == '-')
		flag = -1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str >= 48 && *str <= 57)
	{
		n = n * 10 + (*str - '0');
		str++;
	}
	return (n * flag);
}
