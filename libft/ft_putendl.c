#include "libft.h"
#include <unistd.h>

void	ft_putendl(char const *s)
{
	int	n;

	n = 0;
	if (!s)
		return ;
	while (s[n])
	{
		write(1, &s[n], 1);
		n++;
	}
	write(1, "\n", 1);
}
